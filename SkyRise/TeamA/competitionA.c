task usercontrol()
{
	// User control code here, inside the loop
	SensorValue[ime_lift] = 0;
	int btnDown = 5;
	int btnDown2 = 5;
	int btnDown3 = 5;
	while(1)
	{
		setR(vexRT[Ch2]);
		setL(vexRT[Ch3]);

		if(vexRT[Btn6U] && !vexRT[Btn6D])
		{
			lift(LIFT_SPEED, true);
		}
		else if(vexRT[Btn6D] && !vexRT[Btn6U])
		{
			lift(LIFT_SPEED, false);
		}
		else
		{
			lift(10, true);
		}

		if(!vexRT[Btn8D] && btnDown<0)
		{
			btnDown = 5;
			SensorValue[piston] = !SensorValue[piston];
		}
		else if (vexRT[Btn8D])
		{
			btnDown--;
			wait1Msec(10);
		}

		if(!vexRT[Btn8R] && btnDown2<0)
		{
			btnDown2 = 5;
			driveMotors(-2,98,DRIVE_F);

		}
		else if (vexRT[Btn8R])
		{
			btnDown2--;
			wait1Msec(10);
		}

		if(!vexRT[Btn7U] && btnDown3<0)
		{
			btnDown3 = 5;
			liftDist(7,70,UP);
		}
		else if (vexRT[Btn7U])
		{
			btnDown3--;
			wait1Msec(10);
		}
	}

}
